    end,
    config = function()
      require("which-key").setup({
        preset = "modern",
        spec = {
          { "<leader>b", group = "buffer" },
          { "<leader>c", group = "code" },
          { "<leader>f", group = "find" },
          { "<leader>g", group = "git" },
